export function addNum (a: number, b: number): number {
  return a + b;
}

export function subNum (a: number, b: number): number {
  return a - b;
}

export class NumberOperations {
  public addNum(num1: number, num2: number): number {
    return num1 + num2;
  }

  public minusNum(num1: number, num2: number): number {
    return num1 - num2;
  }
}
